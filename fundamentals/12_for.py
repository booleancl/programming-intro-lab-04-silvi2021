# El for es una estructura de control que nos permite realizar la misma
# accion,pero con elementos diferentes cada vez

veggies = ['Brocolli','Celery','lettucce','Carrot']

for veggie in veggies:
    print('I need to eat more',veggie)

fruits = ['Apple','strawberry','banana','orange']

for fruit in fruits:
    print('I love to eat',fruit)
